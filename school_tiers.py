def normalize_school_name(name: str) -> str:
    cleaned = name.strip().replace(" ", "")
    return ALIASES.get(cleaned, cleaned)


def detect_school_tier(name: str) -> dict:
    school = normalize_school_name(name)
    tags = []
    category = "unknown"
    score_ratio = 0.45

    if school in PROJECT_985:
        tags.extend(["985", "211", "双一流"])
        category = "top_985"
        score_ratio = 1.0
    elif school in PROJECT_211:
        tags.extend(["211", "双一流"])
        category = "project_211"
        score_ratio = 0.88
    elif school in DOUBLE_FIRST_CLASS:
        tags.append("双一流")
        category = "double_first_class"
        score_ratio = 0.8
    elif school.endswith("大学"):
        category = "university"
        score_ratio = 0.62
    elif school.endswith("学院"):
        category = "college"
        score_ratio = 0.38
    elif school:
        category = "other"
        score_ratio = 0.45

    if not school:
        label = "未填写学校名称"
    elif tags:
        label = f"{school}：{' / '.join(tags)}院校"
    else:
        label = f"{school}：已识别学校名称"

    return {
        "school": school,
        "tags": tags,
        "category": category,
        "score_ratio": score_ratio,
        "label": label,
    }
